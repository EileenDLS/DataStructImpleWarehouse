public class Product {
    String UPC;
    String description;
    public void setUPC(String UPC) {
        this.UPC = UPC;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getUPC() {
        return UPC;
    }

    public String getDescription() {
        return description;
    }


}